
public class Virus
{
	private int strength;
	private int speed;
	private int growth;
	
	public Virus(int strength, int speed, int growth)
	{
		this.strength = strength;
		this.speed = speed;
		this.growth = growth;
	}

	public int getStrength()
	{
		return strength;
	}

	public int getSpeed()
	{
		return speed;
	}

	public int getGrowth()
	{
		return growth;
	}
	
	
}
